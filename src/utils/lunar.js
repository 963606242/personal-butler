/**
 * 获取农历年的总天数
 */
function getLunarYearDays(year) {
  let total = 348; // 29天 * 12个月 = 348天
  for (let i = 0x8000; i > 0x8; i >>= 1) {
    total += (lunarInfo[year - 1900] & i) ? 1 : 0;
  }
  return total + getLeapMonthDays(year);
}

/**
 * 获取农历年闰月的天数
 */
function getLeapMonthDays(year) {
  if (getLeapMonth(year)) {
    return (lunarInfo[year - 1900] & 0x10000) ? 30 : 29;
  } else {
    return 0;
  }
}

/**
 * 获取农历年闰哪个月
 */
function getLeapMonth(year) {
  return lunarInfo[year - 1900] & 0xf;
}

/**
 * 获取农历年某个月的天数
 */
function getLunarMonthDays(year, month) {
  return (lunarInfo[year - 1900] & (0x10000 >> month)) ? 30 : 29;
}

/**
 * 公历转农历
 * @param {number} year 公历年份
 * @param {number} month 公历月份 (1-12)
 * @param {number} day 公历日期
 * @returns {Object} 农历信息
 */
export function solarToLunar(year, month, day) {
  // 基准日期：1900年1月31日（农历1900年正月初一）
  const baseDate = new Date(1900, 0, 31);
  const targetDate = new Date(year, month - 1, day);
  
  // 计算相差天数
  let offset = Math.floor((targetDate - baseDate) / 86400000);
  
  if (offset < 0) {
    return null;
  }
  
  let lunarYear = 1900;
  let lunarMonth = 1;
  let lunarDay = 1;
  
  // 确定农历年份
  let yearDays = getLunarYearDays(lunarYear);
  while (offset >= yearDays && lunarYear < 2100) {
    offset -= yearDays;
    lunarYear++;
    yearDays = getLunarYearDays(lunarYear);
  }
  
  if (lunarYear >= 2100) {
    return null;
  }
  
  // 确定农历月份和日期
  const leapMonth = getLeapMonth(lunarYear);
  let isLeapMonth = false;
  let tempOffset = offset;
  
  // 遍历12个月（可能包含闰月）
  for (let i = 1; i <= 12; i++) {
    // 检查是否需要插入闰月
    if (leapMonth > 0 && i === leapMonth + 1 && !isLeapMonth) {
      // 插入闰月
      const leapDays = getLeapMonthDays(lunarYear);
      if (tempOffset < leapDays) {
        // 在闰月中
        lunarMonth = i;
        isLeapMonth = true;
        lunarDay = tempOffset + 1;
        break;
      }
      tempOffset -= leapDays;
      isLeapMonth = false;
    }
    
    // 计算当前月的天数
    const monthDays = getLunarMonthDays(lunarYear, i);
    if (tempOffset < monthDays) {
      // 在当前月中
      lunarMonth = i;
      lunarDay = tempOffset + 1;
      break;
    }
    tempOffset -= monthDays;
  }
  
  // 验证结果
  if (lunarMonth < 1 || lunarMonth > 12 || lunarDay < 1 || lunarDay > 30) {
    return null;
  }
  
  return {
    year: lunarYear,
    month: lunarMonth,
    day: lunarDay,
    isLeapMonth: isLeapMonth,
    monthName: lunarMonthNames[lunarMonth - 1] || '正',
    dayName: lunarDayNames[lunarDay - 1] || '初一',
    fullText: `${isLeapMonth ? '闰' : ''}${lunarMonthNames[lunarMonth - 1] || '正'}月${lunarDayNames[lunarDay - 1] || '初一'}`,
    simpleText: `${lunarMonthNames[lunarMonth - 1] || '正'}.${lunarDay}`
  };
}

/**
 * 获取天干地支年份
 */
export function getGanZhiYear(year) {
  const gan = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
  const zhi = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];
  const ganIndex = (year - 4) % 10;
  const zhiIndex = (year - 4) % 12;
  return `${gan[ganIndex]}${zhi[zhiIndex]}`;
}
